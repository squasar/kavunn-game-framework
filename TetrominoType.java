import java.awt.Color;

public enum TetrominoType {
    I(
        new Color(78, 205, 196),
        new int[][][] {
            { {1, 0}, {1, 1}, {1, 2}, {1, 3} },
            { {0, 2}, {1, 2}, {2, 2}, {3, 2} },
            { {2, 0}, {2, 1}, {2, 2}, {2, 3} },
            { {0, 1}, {1, 1}, {2, 1}, {3, 1} }
        }
    ),
    O(
        new Color(255, 209, 102),
        new int[][][] {
            { {0, 1}, {0, 2}, {1, 1}, {1, 2} },
            { {0, 1}, {0, 2}, {1, 1}, {1, 2} },
            { {0, 1}, {0, 2}, {1, 1}, {1, 2} },
            { {0, 1}, {0, 2}, {1, 1}, {1, 2} }
        }
    ),
    T(
        new Color(199, 125, 255),
        new int[][][] {
            { {0, 1}, {1, 0}, {1, 1}, {1, 2} },
            { {0, 1}, {1, 1}, {1, 2}, {2, 1} },
            { {1, 0}, {1, 1}, {1, 2}, {2, 1} },
            { {0, 1}, {1, 0}, {1, 1}, {2, 1} }
        }
    ),
    S(
        new Color(120, 220, 82),
        new int[][][] {
            { {0, 1}, {0, 2}, {1, 0}, {1, 1} },
            { {0, 1}, {1, 1}, {1, 2}, {2, 2} },
            { {1, 1}, {1, 2}, {2, 0}, {2, 1} },
            { {0, 0}, {1, 0}, {1, 1}, {2, 1} }
        }
    ),
    Z(
        new Color(255, 107, 107),
        new int[][][] {
            { {0, 0}, {0, 1}, {1, 1}, {1, 2} },
            { {0, 2}, {1, 1}, {1, 2}, {2, 1} },
            { {1, 0}, {1, 1}, {2, 1}, {2, 2} },
            { {0, 1}, {1, 0}, {1, 1}, {2, 0} }
        }
    ),
    J(
        new Color(94, 114, 228),
        new int[][][] {
            { {0, 0}, {1, 0}, {1, 1}, {1, 2} },
            { {0, 1}, {0, 2}, {1, 1}, {2, 1} },
            { {1, 0}, {1, 1}, {1, 2}, {2, 2} },
            { {0, 1}, {1, 1}, {2, 0}, {2, 1} }
        }
    ),
    L(
        new Color(255, 159, 67),
        new int[][][] {
            { {0, 2}, {1, 0}, {1, 1}, {1, 2} },
            { {0, 1}, {1, 1}, {2, 1}, {2, 2} },
            { {1, 0}, {1, 1}, {1, 2}, {2, 0} },
            { {0, 0}, {0, 1}, {1, 1}, {2, 1} }
        }
    );

    private final Color color;
    private final int[][][] rotations;

    TetrominoType(Color color, int[][][] rotations) {
        this.color = color;
        this.rotations = rotations;
    }

    public Color color() {
        return color;
    }

    public int[][] cells(int rotation) {
        return rotations[Math.floorMod(rotation, rotations.length)];
    }
}
